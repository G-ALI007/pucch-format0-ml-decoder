"""
================================================================================
PUCCH Format 0 - Machine Learning Decoder
Main Execution Script
================================================================================

This is the main entry point for the PUCCH Format 0 ML Decoder.

It orchestrates the complete pipeline:
    1. System information and configuration display
    2. Data loading from CSV files
    3. Data analysis and validation
    4. Data splitting (train/validation/test)
    5. Data preprocessing
    6. Neural network creation
    7. Model training
    8. Model evaluation on all SNR values
    9. Comparison with correlation-based decoder
    10. Visualization (all plots)
    11. Results saving

Usage:
    python main.py

Requirements:
    - CSV data files generated by MATLAB (pucch_f0_dataset_SNR_XdB.csv)
    - All module files (config.py, data_loader.py, data_preprocessing.py,
      model.py, evaluation.py, visualization.py)

================================================================================
"""

