# SECTION 4: EVALUATION ONLY MODE
# =============================================================================

def run_evaluation_only(
    model_filepath: str = None,
    show_plots: bool = True,
    verbose: bool = True
):
    """
    Run evaluation only using a previously trained model.

    This is useful when you want to re-evaluate or generate new plots
    without retraining the model.

    Parameters:
    -----------
    model_filepath : str, optional
        Path to the trained model
        Default: config.MODELS_DIR + config.MODEL_FILENAME
    show_plots : bool, default=True
        If True, display plots
    verbose : bool, default=True
        If True, print progress

    Returns:
    --------
    results : dict or None
        Dictionary containing evaluation results, or None if failed
    """

    if model_filepath is None:
        model_filepath = config.get_model_filepath()

    print("\n" + "=" * 70)
    print("EVALUATION ONLY MODE")
    print("=" * 70)

    # Set seeds
    set_random_seeds(config.MASTER_SEED, verbose=True)

    # Check model exists
    if not os.path.exists(model_filepath):
        print(f"ERROR: Model file not found: {model_filepath}")
        print("Please train a model first using run_pipeline()")
        return None

    # Load model
    try:
        model = load_saved_model(filepath=model_filepath, verbose=True)
    except Exception as e:
        print(f"ERROR: Failed to load model: {e}")
        return None

    # Check data files
    if not check_data_files(verbose=True):
        return None

    # Load data
    try:
        datasets = load_all_datasets(verbose=True)
    except Exception as e:
        print(f"ERROR: Failed to load data: {e}")
        return None

    # Prepare data
    try:
        X_train, y_train, X_val, y_val, X_test_dict, y_test_dict = prepare_data(
            datasets=datasets,
            verbose=True
        )
    except Exception as e:
        print(f"ERROR: Failed to prepare data: {e}")
        return None

    # Preprocess
    X_train_proc, X_val_proc, X_test_dict_proc, scaler = preprocess_pipeline(
        X_train=X_train,
        X_val=X_val,
        X_test_dict=X_test_dict,
        verbose=True
    )

    # Evaluate
    all_results = evaluate_all_snr(
        model=model,
        X_test_dict=X_test_dict_proc,
        y_test_dict=y_test_dict,
        verbose=True
    )

    # Classification reports
    reports = generate_all_classification_reports(
        all_results=all_results,
        verbose=True
    )

    # Compare with correlation
    comparison = compare_nn_vs_correlation(
        model=model,
        X_test_dict=X_test_dict_proc,
        y_test_dict=y_test_dict,
        X_train=X_train_proc,
        y_train=y_train,
        verbose=True
    )

    # Save results
    save_evaluation_results(
        all_results=all_results,
        comparison=comparison,
        verbose=True
    )

    print("\n" + "=" * 70)
    print("EVALUATION COMPLETE")
    print("=" * 70 + "\n")

    results = {
        'model': model,
        'all_results': all_results,
        'comparison': comparison,
        'reports': reports
    }

    return results


# =============================================================================
# SECTION 5: SCRIPT ENTRY POINT
# =============================================================================

if __name__ == "__main__":
    """
    Main entry point.

    Usage:
        python main.py              # Run full pipeline
        python main.py --eval-only  # Run evaluation only
        python main.py --no-plots   # Run without displaying plots
    """

    # Parse simple command line arguments
    show_plots = True
    eval_only = False

    if len(sys.argv) > 1:
        for arg in sys.argv[1:]:
            if arg == "--eval-only":
                eval_only = True
            elif arg == "--no-plots":
                show_plots = False
            elif arg == "--help":
                print("\nUsage: python main.py [options]")
                print("\nOptions:")
                print("  --eval-only   Run evaluation only (requires trained model)")
                print("  --no-plots    Run without displaying plots")
                print("  --help        Show this help message")
                print()
                sys.exit(0)
            else:
                print(f"Unknown argument: {arg}")
                print("Use --help for usage information")
                sys.exit(1)

    # Run selected mode
    if eval_only:
        results = run_evaluation_only(show_plots=show_plots)
    else:
        results = run_pipeline(show_plots=show_plots)

    # Check if pipeline completed successfully
    if results is not None:
        print("Pipeline completed successfully!")
    else:
        print("Pipeline failed. Please check the error messages above.")
        sys.exit(1)
