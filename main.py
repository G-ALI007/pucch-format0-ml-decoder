# SECTION 2: CHECK DATA FILES
# =============================================================================

def check_data_files(verbose: bool = True) -> bool:
    """
    Check if all required data files exist.

    Parameters:
    -----------
    verbose : bool, default=True
        If True, print file status

    Returns:
    --------
    all_found : bool
        True if all required files exist
    """

    if verbose:
        print("\n" + "=" * 70)
        print("CHECKING DATA FILES")
        print("=" * 70)

    all_found = True
    found_count = 0
    missing_count = 0

    for snr in config.SNR_VALUES:
        filepath = config.get_data_filepath(snr)
        exists = os.path.exists(filepath)

        if exists:
            found_count += 1
            file_size = os.path.getsize(filepath) / (1024 * 1024)  # MB
            if verbose:
                print(
                    f"  [FOUND]   SNR {snr:3d} dB: {filepath} ({file_size:.1f} MB)")
        else:
            missing_count += 1
            all_found = False
            if verbose:
                print(f"  [MISSING] SNR {snr:3d} dB: {filepath}")

    if verbose:
        print(f"\nFound: {found_count}/{len(config.SNR_VALUES)} files")

        if not all_found:
            print("\nWARNING: Some data files are missing!")
            print("Please run the MATLAB data generation script first.")
            print(f"Expected files in: {os.path.abspath(config.DATA_DIR)}")

        print("=" * 70 + "\n")

    return all_found


