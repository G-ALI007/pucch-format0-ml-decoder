def run_multi_experiments(
    num_runs: Optional[int] = None,
    verbose: bool = True
) -> Optional[Dict[str, Any]]:
    """
    Run multiple experiments with different random seeds.

    Parameters:
    -----------
    num_runs : int, optional
        Number of experiment runs
        If None, uses config_multi_ue.NUM_EXPERIMENT_RUNS
    verbose : bool, default=True
        If True, print progress

    Returns:
    --------
    results : dict or None
        Dictionary containing all results, or None if failed
    """

    # Set number of runs
    if num_runs is None:
        num_runs = config_multi_ue.NUM_EXPERIMENT_RUNS

    pipeline_start = time.time()

    print(f"\n{'#'*80}")
    print(f"{'#':<20}MULTI-EXPERIMENT RUNNER (Multi-User){'#':>39}")
    print(f"{'#'*80}")

    print(
        f"\nTimestamp: {datetime.datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    print(f"Number of runs: {num_runs}")
    print(f"Base seed: {config_multi_ue.MASTER_SEED}")
    print(f"Seed increment: {config_multi_ue.SEED_INCREMENT}")
    print(f"Scenario: {config_multi_ue.CURRENT_SCENARIO_KEY}")
    print(f"Number of users: {config_multi_ue.NUM_USERS}")

    # Check data files exist
    print(f"\n--- Checking Data Files ---")
    files_exist = True
    for snr in config_multi_ue.SNR_VALUES:
        filepath = config_multi_ue.get_data_filepath(snr)
        if not os.path.exists(filepath):
            print(f"  [MISSING] {filepath}")
            files_exist = False

    if not files_exist:
        print(f"\n❌ ERROR: Required data files not found!")
        print(f"Please run MATLAB multi-user data generation first.")
        return None

    print(f"  All data files found ✓")

    # Run all experiments
    all_results = []

    for run_index in range(num_runs):
        # Get seed for this run
        seed = config_multi_ue.get_run_seed(run_index)

        # Run experiment
        results = run_single_experiment(
            run_index=run_index,
            seed=seed,
            verbose=verbose
        )

        if results is not None:
            all_results.append(results)
        else:
            print(f"\n⚠️  WARNING: Run {run_index + 1} failed. Continuing...")

    # Check if any runs succeeded
    if len(all_results) == 0:
        print(f"\n❌ ERROR: All experiment runs failed!")
        return None

    # Compute statistical summary
    print(f"\n{'='*80}")
    print(f"COMPUTING STATISTICAL SUMMARY")
    print(f"{'='*80}")

    summary_df, training_stats_df = compute_statistical_summary(all_results)

    # Print summary
    print_statistical_summary(summary_df, training_stats_df, len(all_results))

    # Save results
    save_multi_experiment_results(
        all_results=all_results,
        summary_df=summary_df,
        training_stats_df=training_stats_df,
        verbose=verbose
    )

    # Final summary
    pipeline_time = time.time() - pipeline_start

    print(f"\n{'#'*80}")
    print(f"{'#':<25}MULTI-EXPERIMENT COMPLETE{'#':>29}")
    print(f"{'#'*80}")

    print(
        f"\nTotal pipeline time: {pipeline_time:.1f}s ({pipeline_time/3600:.2f} hours)")
    print(f"Successful runs: {len(all_results)}/{num_runs}")
    print(f"Average time per run: {pipeline_time/len(all_results):.1f}s")

    print(f"\n--- Key Findings ---")
    avg_gain = np.mean([np.mean(r['gain']) for r in all_results])
    std_gain = np.std([np.mean(r['gain']) for r in all_results])
    print(
        f"  Average Gain over Correlation: {avg_gain:.2f}% ± {std_gain:.2f}%")

    avg_nn_acc = np.mean([np.mean(r['nn_accuracy']) for r in all_results])
    std_nn_acc = np.std([np.mean(r['nn_accuracy']) for r in all_results])
    print(f"  Average NN Accuracy: {avg_nn_acc:.2f}% ± {std_nn_acc:.2f}%")

    print(f"\n{'#'*80}\n")

    return {
        'all_results': all_results,
        'summary_df': summary_df,
        'training_stats_df': training_stats_df,
        'pipeline_time': pipeline_time,
        'num_runs': num_runs,
        'successful_runs': len(all_results)
    }


