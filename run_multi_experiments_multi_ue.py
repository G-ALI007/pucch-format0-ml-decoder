def compute_statistical_summary(all_results: List[Dict]) -> pd.DataFrame:
    """
    Compute statistical summary across all experiment runs.

    Parameters:
    -----------
    all_results : list of dict
        List of results from each experiment run

    Returns:
    --------
    summary_df : pd.DataFrame
        DataFrame with mean ± std for all metrics
    """

    snr_values = config_multi_ue.SNR_VALUES
    num_runs = len(all_results)

    # Extract metrics per SNR
    nn_acc_matrix = np.array([r['nn_accuracy']
                             for r in all_results])  # (num_runs, num_snr)
    corr_acc_matrix = np.array([r['corr_accuracy'] for r in all_results])
    gain_matrix = np.array([r['gain'] for r in all_results])

    # Compute statistics
    summary_data = []

    for i, snr in enumerate(snr_values):
        row = {
            'SNR_dB': snr,

            # Neural Network Accuracy
            'NN_Accuracy_Mean': float(np.mean(nn_acc_matrix[:, i])),
            'NN_Accuracy_Std': float(np.std(nn_acc_matrix[:, i])),
            'NN_Accuracy_Min': float(np.min(nn_acc_matrix[:, i])),
            'NN_Accuracy_Max': float(np.max(nn_acc_matrix[:, i])),

            # Correlation Accuracy
            'Corr_Accuracy_Mean': float(np.mean(corr_acc_matrix[:, i])),
            'Corr_Accuracy_Std': float(np.std(corr_acc_matrix[:, i])),

            # Gain
            'Gain_Mean': float(np.mean(gain_matrix[:, i])),
            'Gain_Std': float(np.std(gain_matrix[:, i])),
            'Gain_Min': float(np.min(gain_matrix[:, i])),
            'Gain_Max': float(np.max(gain_matrix[:, i])),

            # 95% Confidence Interval for NN Accuracy
            'NN_Accuracy_95CI_Lower': float(np.mean(nn_acc_matrix[:, i]) - 1.96 * np.std(nn_acc_matrix[:, i]) / np.sqrt(num_runs)),
            'NN_Accuracy_95CI_Upper': float(np.mean(nn_acc_matrix[:, i]) + 1.96 * np.std(nn_acc_matrix[:, i]) / np.sqrt(num_runs)),
        }
        summary_data.append(row)

    # Training statistics
    training_stats = {
        'Metric': ['Best Val Acc', 'Training Time (s)', 'Best Epoch'],
        'Mean': [
            float(np.mean([r['best_val_acc'] for r in all_results])),
            float(np.mean([r['training_time'] for r in all_results])),
            float(np.mean([r['best_epoch'] for r in all_results]))
        ],
        'Std': [
            float(np.std([r['best_val_acc'] for r in all_results])),
            float(np.std([r['training_time'] for r in all_results])),
            float(np.std([r['best_epoch'] for r in all_results]))
        ],
        'Min': [
            float(np.min([r['best_val_acc'] for r in all_results])),
            float(np.min([r['training_time'] for r in all_results])),
            float(np.min([r['best_epoch'] for r in all_results]))
        ],
        'Max': [
            float(np.max([r['best_val_acc'] for r in all_results])),
            float(np.max([r['training_time'] for r in all_results])),
            float(np.max([r['best_epoch'] for r in all_results]))
        ]
    }

    summary_df = pd.DataFrame(summary_data)
    training_stats_df = pd.DataFrame(training_stats)

    return summary_df, training_stats_df


