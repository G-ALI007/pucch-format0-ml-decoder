def print_statistical_summary(
    summary_df: pd.DataFrame,
    training_stats_df: pd.DataFrame,
    num_runs: int
) -> None:
    """
    Print statistical summary to console.

    Parameters:
    -----------
    summary_df : pd.DataFrame
        Statistical summary per SNR
    training_stats_df : pd.DataFrame
        Training statistics
    num_runs : int
        Number of experiment runs
    """

    print(f"\n{'='*80}")
    print(f"STATISTICAL SUMMARY ({num_runs} EXPERIMENT RUNS)")
    print(f"{'='*80}")

    print(f"\n--- Neural Network Accuracy vs SNR ---")
    print(f"{'SNR (dB)': <10}{'Mean (%)': <12}{'Std (%)': <12}{'95% CI': <25}")
    print("-" * 59)

    for _, row in summary_df.iterrows():
        snr = int(row['SNR_dB'])
        mean = row['NN_Accuracy_Mean']
        std = row['NN_Accuracy_Std']
        ci_lower = row['NN_Accuracy_95CI_Lower']
        ci_upper = row['NN_Accuracy_95CI_Upper']

        print(
            f"{snr: <10}{mean: <12.2f}{std: <12.2f}[{ci_lower:.2f}, {ci_upper:.2f}]")

    print("-" * 59)

    print(f"\n--- Performance Gain Over Correlation Decoder ---")
    print(f"{'SNR (dB)': <10}{'Mean Gain (%)': <15}{'Std (%)': <12}")
    print("-" * 37)

    for _, row in summary_df.iterrows():
        snr = int(row['SNR_dB'])
        gain_mean = row['Gain_Mean']
        gain_std = row['Gain_Std']
        print(f"{snr: <10}{gain_mean: <15.2f}{gain_std: <12.2f}")

    print("-" * 37)

    print(f"\n--- Training Statistics ---")
    print(training_stats_df.to_string(index=False))

    print(f"\n{'='*80}\n")


