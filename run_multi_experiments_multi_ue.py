def save_multi_experiment_results(
    all_results: List[Dict],
    summary_df: pd.DataFrame,
    training_stats_df: pd.DataFrame,
    verbose: bool = True
) -> None:
    """
    Save all multi-experiment results to files.

    Parameters:
    -----------
    all_results : list of dict
        Individual run results
    summary_df : pd.DataFrame
        Statistical summary
    training_stats_df : pd.DataFrame
        Training statistics
    verbose : bool, default=True
        Print save confirmation
    """

    # Create output directory
    stats_dir = "./results_multi_ue/statistical/"
    os.makedirs(stats_dir, exist_ok=True)

    # Save individual runs
    runs_filepath = os.path.join(stats_dir, "all_runs_raw.csv")
    runs_data = []

    for results in all_results:
        for i, snr in enumerate(results['snr_values']):
            row = {
                'run_index': results['run_index'],
                'seed': results['seed'],
                'SNR_dB': snr,
                'NN_Accuracy_pct': results['nn_accuracy'][i],
                'Corr_Accuracy_pct': results['corr_accuracy'][i],
                'Gain_pct': results['gain'][i],
                'best_val_acc': results['best_val_acc'],
                'training_time': results['training_time']
            }
            runs_data.append(row)

    runs_df = pd.DataFrame(runs_data)
    runs_df.to_csv(runs_filepath, index=False, float_format='%.4f')

    # Save statistical summary
    summary_filepath = os.path.join(stats_dir, "statistical_summary.csv")
    summary_df.to_csv(summary_filepath, index=False, float_format='%.4f')

    # Save training statistics
    training_filepath = os.path.join(stats_dir, "training_statistics.csv")
    training_stats_df.to_csv(
        training_filepath, index=False, float_format='%.4f')

    # Save LaTeX table for paper
    latex_filepath = os.path.join(stats_dir, "results_table.tex")
    with open(latex_filepath, 'w') as f:
        f.write("\\begin{table}[h]\n")
        f.write("\\centering\n")
        f.write(
            "\\caption{Multi-User PUCCH Format 0: Neural Network vs Correlation Decoder}\n")
        f.write("\\label{tab:multi_ue_results}\n")
        f.write("\\begin{tabular}{c|c|c|c}\n")
        f.write("\\hline\n")
        f.write(
            "\\textbf{SNR (dB)} & \\textbf{NN Accuracy (\\%)} & \\textbf{Correlation (\\%)} & \\textbf{Gain (\\%)} \\\\\n")
        f.write("\\hline\n")

        for _, row in summary_df.iterrows():
            snr = int(row['SNR_dB'])
            nn_acc = row['NN_Accuracy_Mean']
            nn_std = row['NN_Accuracy_Std']
            corr_acc = row['Corr_Accuracy_Mean']
            gain = row['Gain_Mean']
            gain_std = row['Gain_Std']

            f.write(
                f"{snr} & {nn_acc:.2f} $\\pm$ {nn_std:.2f} & {corr_acc:.2f} & {gain:.2f} $\\pm$ {gain_std:.2f} \\\\\n")

        f.write("\\hline\n")
        f.write("\\end{tabular}\n")
        f.write("\\end{table}\n")

    if verbose:
        print(f"\n{'='*80}")
        print("RESULTS SAVED")
        print(f"{'='*80}")
        print(f"  Individual runs: {runs_filepath}")
        print(f"  Statistical summary: {summary_filepath}")
        print(f"  Training statistics: {training_filepath}")
        print(f"  LaTeX table: {latex_filepath}")
        print(f"{'='*80}\n")


