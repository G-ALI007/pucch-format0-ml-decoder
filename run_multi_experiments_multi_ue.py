def run_single_experiment(
    run_index: int,
    seed: int,
    verbose: bool = True
) -> Optional[Dict[str, Any]]:
    """
    Run a single experiment with a specific random seed.

    Parameters:
    -----------
    run_index : int
        Experiment run index (0, 1, 2, ...)
    seed : int
        Random seed for this run
    verbose : bool, default=True
        If True, print progress

    Returns:
    --------
    results : dict or None
        Dictionary containing all metrics for this run, or None if failed
    """

    experiment_start = time.time()

    if verbose:
        print(f"\n{'='*80}")
        print(
            f"EXPERIMENT RUN {run_index + 1}/{config_multi_ue.NUM_EXPERIMENT_RUNS}")
        print(f"Seed: {seed}")
        print(
            f"Timestamp: {datetime.datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
        print(f"{'='*80}\n")

    try:
        # Set random seeds
        set_random_seeds(seed=seed, verbose=False)

        # Load data
        if verbose:
            print("Loading data...")
        datasets = load_all_multi_ue_datasets(verbose=False)

        # Prepare data
        if verbose:
            print("Preparing data splits...")
        X_train, y_train, X_val, y_val, X_test_dict, y_test_dict = prepare_data(
            datasets=datasets, verbose=False)

        # Preprocess
        if verbose:
            print("Preprocessing data...")
        X_train_proc, X_val_proc, X_test_dict_proc, scaler = preprocess_pipeline(
            X_train=X_train, X_val=X_val, X_test_dict=X_test_dict, verbose=False)

        # Create model
        if verbose:
            print("Creating model...")
        model = create_model(print_summary=False)

        # Train model
        if verbose:
            print("Training model...")
        model_filepath = config_multi_ue.get_model_filepath(
            run_index=run_index)
        history, training_time = train_model(
            model=model,
            X_train=X_train_proc,
            y_train=y_train,
            X_val=X_val_proc,
            y_val=y_val,
            model_filepath=model_filepath,
            verbose=0
        )

        # Load best model
        best_model = load_saved_model(filepath=model_filepath, verbose=False)

        # Evaluate
        if verbose:
            print("Evaluating model...")
        all_results = evaluate_all_snr(
            model=best_model,
            X_test_dict=X_test_dict_proc,
            y_test_dict=y_test_dict,
            verbose=False
        )

        # Compare with correlation
        if verbose:
            print("Comparing with correlation decoder...")
        comparison = compare_nn_vs_correlation(
            model=best_model,
            X_test_dict=X_test_dict_proc,
            y_test_dict=y_test_dict,
            X_train=X_train_proc,
            y_train=y_train,
            verbose=False
        )

        # Extract key metrics
        results = {
            'run_index': run_index,
            'seed': seed,
            'experiment_time': time.time() - experiment_start,
            'training_time': training_time,
            'best_val_acc': max(history['val_accuracy']) * 100,
            'best_epoch': int(np.argmax(history['val_accuracy']) + 1),
            'final_train_acc': history['accuracy'][-1] * 100,
            'final_val_acc': history['val_accuracy'][-1] * 100,
            'snr_values': config_multi_ue.SNR_VALUES,
            'nn_accuracy': comparison['nn_accuracy'],  # List per SNR
            'corr_accuracy': comparison['corr_accuracy'],  # List per SNR
            'gain': [comparison['nn_accuracy'][i] - comparison['corr_accuracy'][i]
                     for i in range(len(comparison['snr']))],
            'history': history
        }

        if verbose:
            print(f"\nRun {run_index + 1} complete!")
            print(f"  Time: {results['experiment_time']:.1f}s")
            print(f"  Best Val Acc: {results['best_val_acc']:.2f}%")
            print(f"  Avg NN Accuracy: {np.mean(results['nn_accuracy']):.2f}%")
            print(f"  Avg Gain: {np.mean(results['gain']):.2f}%")

        return results

    except Exception as e:
        print(f"\n❌ ERROR in run {run_index}: {e}")
        import traceback
        traceback.print_exc()
        return None


