"""
PUCCH Format 0 - Multi-User Machine Learning Decoder
Main Execution Script (Research-Grade Implementation)

Novel Contribution:
==================
This script implements a machine learning-based decoder for PUCCH Format 0
in multi-user scenarios where multiple User Equipments (UEs) transmit 
simultaneously on the same time-frequency resources using different 
Initial Cyclic Shifts (m0).

Research Objectives:
-------------------
1. Interference Robustness: Train ML model to decode target user signal
   in the presence of inter-user interference from 1-2 interfering users.

2. Baseline Comparison: Comprehensive comparison with traditional 
   correlation-based decoder to quantify ML performance gains.

3. Multi-SNR Generalization: Model trained on single SNR (10 dB), 
   evaluated across wide SNR range (0-20 dB) to assess generalization.

4. Reproducibility: Full experiment logging, seed control, and result 
   saving for research reproducibility and peer verification.

Supported Scenarios:
-------------------
- 2 Users: Target (m0=0) + 1 Interferer (m0=1)
- 3 Users: Target (m0=0) + 2 Interferers (m0=1, m0=2)

Expected Results for Publication:
--------------------------------
- Accuracy vs SNR curves for NN and Correlation decoder
- Performance gain analysis (NN - Correlation)
- Confusion matrices per SNR value
- Computational complexity comparison

Usage:
------
    python main_multi_ue.py              # Run full pipeline with plots
    python main_multi_ue.py --no-plots   # Run without displaying plots
    python main_multi_ue.py --eval-only  # Evaluation only (requires trained model)
    python main_multi_ue.py --help       # Show help message

Requirements:
------------
- Multi-user CSV data files generated by MATLAB:
  * pucch_f0_2users_SNR_XdB.csv (for 2-user scenario)
  * pucch_f0_3users_SNR_XdB.csv (for 3-user scenario)
  
- All multi_ue module files:
  * config_multi_ue.py
  * data_loader_multi_ue.py
  * data_preprocessing_multi_ue.py
  * evaluation_multi_ue.py
  * model.py (base module)

Author: Research Team
Date: 2024
Version: 1.0
================================================================================
"""

