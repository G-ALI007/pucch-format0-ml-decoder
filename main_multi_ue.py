def check_multi_ue_data_files(verbose: bool = True) -> bool:
    """
    Check if all required multi-user data files exist.

    Parameters:
    -----------
    verbose : bool, default=True
        If True, print file status

    Returns:
    --------
    all_found : bool
        True if all required files exist
    """

    if verbose:
        print("\n" + "=" * 80)
        print("CHECKING MULTI-USER DATA FILES")
        print("=" * 80)

    all_found = True
    found_count = 0
    missing_count = 0

    scenario = config_multi_ue.CURRENT_SCENARIO_KEY

    for snr in config_multi_ue.SNR_VALUES:
        filepath = config_multi_ue.get_data_filepath(snr, scenario=scenario)
        exists = os.path.exists(filepath)

        if exists:
            found_count += 1
            file_size = os.path.getsize(filepath) / (1024 * 1024)  # MB
            if verbose:
                print(
                    f"  [FOUND]   SNR {snr:3d} dB: {filepath} ({file_size:.1f} MB)")
        else:
            missing_count += 1
            all_found = False
            if verbose:
                print(f"  [MISSING] SNR {snr:3d} dB: {filepath}")

    if verbose:
        print(
            f"\nFound: {found_count}/{len(config_multi_ue.SNR_VALUES)} files")

        if not all_found:
            print("\n⚠️  WARNING: Some data files are missing!")
            print("Please run the MATLAB multi-user data generation script first.")
            print(
                f"Expected directory: {os.path.abspath(config_multi_ue.DATA_DIR)}")
            print(f"Expected pattern: pucch_f0_{scenario}_SNR_XdB.csv")

        print("=" * 80 + "\n")

    return all_found


