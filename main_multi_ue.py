def run_multi_ue_pipeline(
    show_plots: bool = True,
    verbose: bool = True
) -> Optional[Dict[str, Any]]:
    """
    Run the complete Multi-User ML pipeline.

    This function executes all steps in order:
        1. Print system info and configuration
        2. Check and load multi-user data files
        3. Analyze training dataset
        4. Prepare data splits
        5. Preprocess data
        6. Create neural network model
        7. Train model
        8. Load best model
        9. Evaluate on all SNR values
        10. Generate classification reports
        11. Compare with correlation decoder
        12. Generate plots (if available)
        13. Save all results

    Parameters:
    -----------
    show_plots : bool, default=True
        If True, display plots during execution
        Set to False for batch/automated runs
    verbose : bool, default=True
        If True, print detailed progress information

    Returns:
    --------
    results : dict or None
        Dictionary containing all outputs:
        - 'model': Trained model
        - 'history': Training history
        - 'training_time': Training time in seconds
        - 'all_results': Evaluation results per SNR
        - 'comparison': NN vs Correlation comparison
        - 'training_summary': Summary of training
        - 'scenario': Multi-user scenario info

        Returns None if pipeline fails.
    """

    pipeline_start_time = time.time()

    # =========================================================================
    # STEP 1: System Information and Configuration
    # =========================================================================

    if verbose:
        print("\n")
        print("#" * 80)
        print("#" + " " * 25 + "STARTING MULTI-USER PIPELINE" + " " * 23 + "#")
        print("#" * 80)

    print_system_info()
    config_multi_ue.print_config()

    # Validate configuration
    if not config_multi_ue.validate_config():
        print("❌ ERROR: Configuration validation failed. Aborting.")
        return None

    # Create output directories
    config_multi_ue.create_directories()

    # =========================================================================
    # STEP 2: Set Random Seeds
    # =========================================================================

    if verbose:
        print("\n" + "=" * 80)
        print("STEP 1: Setting Random Seeds")
        print("=" * 80)

    set_random_seeds(seed=config_multi_ue.MASTER_SEED, verbose=True)

    # =========================================================================
    # STEP 3: Check and Load Data Files
    # =========================================================================

    if verbose:
        print("\n" + "=" * 80)
        print("STEP 2: Loading Multi-User Data")
        print("=" * 80)

    # Check files exist
    files_exist = check_multi_ue_data_files(verbose=True)

    if not files_exist:
        print("\n❌ ERROR: Required multi-user data files not found!")
        print("Please run the MATLAB multi-user data generation script first.")
        return None

    # Load all datasets
    try:
        datasets = load_all_multi_ue_datasets(
            snr_values=config_multi_ue.SNR_VALUES,
            scenario=config_multi_ue.CURRENT_SCENARIO_KEY,
            data_dir=config_multi_ue.DATA_DIR,
            validate=True,
            verbose=True
        )
    except Exception as e:
        print(f"\n❌ ERROR: Failed to load datasets: {e}")
        return None

    # Verify training SNR is available
    if config_multi_ue.TRAIN_SNR not in datasets:
        print(
            f"\n❌ ERROR: Training SNR {config_multi_ue.TRAIN_SNR} dB not loaded!")
        print(f"Available SNR values: {list(datasets.keys())}")
        return None

    # =========================================================================
    # STEP 4: Analyze Training Dataset
    # =========================================================================

    if verbose:
        print("\n" + "=" * 80)
        print("STEP 3: Analyzing Training Dataset")
        print("=" * 80)

    train_stats = analyze_multi_ue_dataset(
        X=datasets[config_multi_ue.TRAIN_SNR][0],
        y=datasets[config_multi_ue.TRAIN_SNR][1],
        scenario=config_multi_ue.CURRENT_SCENARIO_KEY,
        name=f"Training Data (SNR = {config_multi_ue.TRAIN_SNR} dB)",
        verbose=True
    )

    # Check for data quality issues
    if train_stats['num_nan'] > 0:
        print("⚠️  WARNING: NaN values found in training data!")
    if train_stats['num_inf'] > 0:
        print("⚠️  WARNING: Inf values found in training data!")
    if not train_stats['is_balanced']:
        print("⚠️  WARNING: Training data is not balanced!")

    # =========================================================================
    # STEP 5: Prepare Data Splits
    # =========================================================================

    if verbose:
        print("\n" + "=" * 80)
        print("STEP 4: Preparing Data Splits")
        print("=" * 80)

    try:
        X_train, y_train, X_val, y_val, X_test_dict, y_test_dict = prepare_data(
            datasets=datasets,
            train_snr=config_multi_ue.TRAIN_SNR,
            train_ratio=config_multi_ue.TRAIN_RATIO,
            random_seed=config_multi_ue.SKLEARN_SEED,
            verbose=True
        )
    except Exception as e:
        print(f"\n❌ ERROR: Failed to prepare data: {e}")
        return None

    # =========================================================================
    # STEP 6: Preprocess Data
    # =========================================================================

    if verbose:
        print("\n" + "=" * 80)
        print("STEP 5: Preprocessing Data")
        print("=" * 80)

    X_train_proc, X_val_proc, X_test_dict_proc, scaler = preprocess_pipeline(
        X_train=X_train,
        X_val=X_val,
        X_test_dict=X_test_dict,
        normalize=config_multi_ue.NORMALIZE_FEATURES,
        verbose=True
    )

    # =========================================================================
    # STEP 7: Create Neural Network Model
    # =========================================================================

    if verbose:
        print("\n" + "=" * 80)
        print("STEP 6: Creating Neural Network Model")
        print("=" * 80)

    model = create_model(
        input_size=config_multi_ue.INPUT_SIZE,
        hidden_layers=config_multi_ue.HIDDEN_LAYERS,
        output_size=config_multi_ue.OUTPUT_SIZE,
        hidden_activation=config_multi_ue.HIDDEN_ACTIVATION,
        output_activation=config_multi_ue.OUTPUT_ACTIVATION,
        dropout_rate=config_multi_ue.DROPOUT_RATE,
        use_dropout=config_multi_ue.USE_DROPOUT,
        kernel_initializer=config_multi_ue.KERNEL_INITIALIZER,
        learning_rate=config_multi_ue.LEARNING_RATE,
        momentum=config_multi_ue.MOMENTUM,
        use_nesterov=config_multi_ue.USE_NESTEROV,
        print_summary=True
    )

    # =========================================================================
    # STEP 8: Train Model
    # =========================================================================

    if verbose:
        print("\n" + "=" * 80)
        print("STEP 7: Training Model")
        print("=" * 80)

    model_filepath = config_multi_ue.get_model_filepath()

    try:
        history, training_time = train_model(
            model=model,
            X_train=X_train_proc,
            y_train=y_train,
            X_val=X_val_proc,
            y_val=y_val,
            epochs=config_multi_ue.NUM_EPOCHS,
            batch_size=config_multi_ue.BATCH_SIZE,
            model_filepath=model_filepath,
            verbose=1
        )
    except Exception as e:
        print(f"\n❌ ERROR: Training failed: {e}")
        return None

    # Get training summary
    training_summary = get_training_summary(history)

    # =========================================================================
    # STEP 9: Load Best Model
    # =========================================================================

    if verbose:
        print("\n" + "=" * 80)
        print("STEP 8: Loading Best Model")
        print("=" * 80)

    try:
        best_model = load_saved_model(filepath=model_filepath, verbose=True)
    except FileNotFoundError:
        print("⚠️  WARNING: Best model file not found. Using last training state.")
        best_model = model

    # =========================================================================
    # STEP 10: Evaluate on All SNR Values
    # =========================================================================

    if verbose:
        print("\n" + "=" * 80)
        print("STEP 9: Evaluating Model")
        print("=" * 80)

    all_results = evaluate_all_snr(
        model=best_model,
        X_test_dict=X_test_dict_proc,
        y_test_dict=y_test_dict,
        verbose=True
    )

    # =========================================================================
    # STEP 11: Generate Classification Reports
    # =========================================================================

    if verbose:
        print("\n" + "=" * 80)
        print("STEP 10: Classification Reports")
        print("=" * 80)

    reports = generate_all_classification_reports(
        all_results=all_results,
        verbose=True
    )

    # =========================================================================
    # STEP 12: Compare with Correlation Decoder
    # =========================================================================

    if verbose:
        print("\n" + "=" * 80)
        print("STEP 11: Comparing with Correlation Decoder")
        print("=" * 80)

    comparison = compare_nn_vs_correlation(
        model=best_model,
        X_test_dict=X_test_dict_proc,
        y_test_dict=y_test_dict,
        X_train=X_train_proc,
        y_train=y_train,
        verbose=True
    )

    # =========================================================================
    # STEP 13: Generate All Plots
    # =========================================================================

    if VISUALIZATION_AVAILABLE and show_plots:
        if verbose:
            print("\n" + "=" * 80)
            print("STEP 12: Generating Plots")
            print("=" * 80)

        try:
            from visualization import generate_all_plots
            generate_all_plots(
                history=history,
                all_results=all_results,
                comparison=comparison,
                show_plots=show_plots,
                verbose=True
            )
        except Exception as e:
            print(f"⚠️  WARNING: Plot generation failed: {e}")
    else:
        if verbose:
            print("\n" + "=" * 80)
            print("STEP 12: Skipping Plots")
            print("=" * 80)
            print("Visualization module not available or plots disabled.")

    # =========================================================================
    # STEP 14: Save All Results
    # =========================================================================

    if verbose:
        print("\n" + "=" * 80)
        print("STEP 13: Saving Results")
        print("=" * 80)

    # Save evaluation results CSV
    results_df = save_evaluation_results(
        all_results=all_results,
        comparison=comparison,
        filepath=os.path.join(
            config_multi_ue.RESULTS_DIR,
            config_multi_ue.RESULTS_FILENAME
        ),
        verbose=True
    )

    # Save confusion matrices
    save_confusion_matrices(
        all_results=all_results,
        filepath=os.path.join(
            config_multi_ue.RESULTS_DIR,
            "confusion_matrices_multi_ue.txt"
        ),
        verbose=True
    )

    # Save experiment summary
    save_experiment_summary(
        all_results=all_results,
        comparison=comparison,
        training_time=training_time,
        history=history,
        filepath=os.path.join(
            config_multi_ue.RESULTS_DIR,
            "experiment_summary_multi_ue.txt"
        ),
        verbose=True
    )

    # =========================================================================
    # FINAL SUMMARY
    # =========================================================================

    pipeline_time = time.time() - pipeline_start_time

    print("\n")
    print("#" * 80)
    print("#" + " " * 25 + "MULTI-USER PIPELINE COMPLETE" + " " * 21 + "#")
    print("#" * 80)

    print(
        f"\nTotal pipeline time: {pipeline_time:.1f} seconds ({pipeline_time/60:.2f} minutes)")
    print(
        f"\nTimestamp: {datetime.datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")

    # Training summary
    print(f"\n--- Training Summary ---")
    print(f"Epochs completed: {training_summary['epochs_completed']}")
    print(f"Training time: {training_time:.1f} seconds")
    print(f"Best validation accuracy: {training_summary['best_val_acc']:.2f}%  "
          f"(epoch {training_summary['best_epoch']})")

    # Results summary
    print(f"\n--- Multi-User Results Summary ---")
    print(f"Scenario: {config_multi_ue.CURRENT_SCENARIO_KEY}")
    print(f"Number of Users: {config_multi_ue.NUM_USERS}")
    print(f"Target User m0: {config_multi_ue.TARGET_USER_M0}")
    print(
        f"\n{'SNR (dB)': <10}{'NN Acc (%)': <12}{'Corr Acc (%)': <14}{'Gain (%)': <10}")
    print("-" * 46)
    for i, snr in enumerate(comparison['snr']):
        nn_acc = comparison['nn_accuracy'][i]
        corr_acc = comparison['corr_accuracy'][i]
        gain = nn_acc - corr_acc
        print(f"{snr: <10}{nn_acc: <12.2f}{corr_acc: <14.2f}{gain: <+10.2f}")
    print("-" * 46)

    # Average gain
    avg_gain = np.mean([comparison['nn_accuracy'][i] - comparison['corr_accuracy'][i]
                        for i in range(len(comparison['snr']))])
    print(f"\nAverage Gain: {avg_gain:+.2f}%")

    # Output files
    print(f"\n--- Output Files ---")
    print(f"Model: {model_filepath}")
    print(
        f"Results: {os.path.join(config_multi_ue.RESULTS_DIR, config_multi_ue.RESULTS_FILENAME)}")
    print(f"Plots: {config_multi_ue.PLOTS_DIR}")
    print(f"Logs: {config_multi_ue.LOGS_DIR}")
    print(
        f"Summary: {os.path.join(config_multi_ue.RESULTS_DIR, 'experiment_summary_multi_ue.txt')}")

    print("\n" + "#" * 80 + "\n")

    # Prepare return dictionary
    results = {
        'model': best_model,
        'history': history,
        'training_time': training_time,
        'training_summary': training_summary,
        'all_results': all_results,
        'comparison': comparison,
        'reports': reports,
        'results_df': results_df,
        'scaler': scaler,
        'X_train': X_train_proc,
        'y_train': y_train,
        'X_val': X_val_proc,
        'y_val': y_val,
        'X_test_dict': X_test_dict_proc,
        'y_test_dict': y_test_dict,
        'pipeline_time': pipeline_time,
        'scenario': config_multi_ue.CURRENT_SCENARIO_KEY,
        'num_users': config_multi_ue.NUM_USERS
    }

    return results


